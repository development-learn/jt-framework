package io.github.hylexus.jt.jt808.support.exception;

import io.github.hylexus.jt.exception.AbstractJtException;

public class Jt808AnnotationArgumentResolveException extends AbstractJtException {
    public Jt808AnnotationArgumentResolveException() {
    }

    public Jt808AnnotationArgumentResolveException(String message) {
        super(message);
    }

    public Jt808AnnotationArgumentResolveException(String message, Throwable cause) {
        super(message, cause);
    }

    public Jt808AnnotationArgumentResolveException(Throwable cause) {
        super(cause);
    }

    public Jt808AnnotationArgumentResolveException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
